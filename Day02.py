def find_duplicates(box_id):
    # Returns a (bool, bool) tuple representing whether there are exactly
    # 2 and 3 of a letter
    let_dict = {}
    two = False
    three = False
    for letter in box_id:
        try:
            let_dict[letter] += 1
        except:
            let_dict[letter] = 1
    for k, v in let_dict.items():
        if v == 2:
            two = True
        elif v == 3:
            three = True
    return (two, three)

def count_checksum(s):
    two = 0
    three = 0
    s = s.split("\n")
    for box in s:
        dup_result = find_duplicates(box)
        if dup_result[0]:
            two += 1
        if dup_result[1]:
            three += 1
    return two * three

